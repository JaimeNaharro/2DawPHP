<?php
declare (strict_types=1);
function sumar($num1,$num2):float{
    return $num1+$num2;
}
function restar($num1, $num2):float{    
    return $num1-$num2;
}
function multiplicar($num1, $num2):float{
    
    return $num1*$num2;
}
function dividir($num1, $num2):float{
    return $num1/$num2;
}

?>